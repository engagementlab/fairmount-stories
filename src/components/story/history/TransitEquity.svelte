	<h1>Transit Equity</h1>
	<p>
		Transit Equity is the fair, reliable and accessible mobility available to
		members of society, but especially disenfranchised communities; and the
		public interest opportunities invested in the transportation interests of
		underserved communities
	</p>
	<i>
		“The term “equity” means the consistent and systematic fair, just, and
		impartial treatment of all individuals, including individuals who belong to
		underserved communities that have been denied such treatment.”
	</i>
	<p class="label">— Department of Transportation</p>
